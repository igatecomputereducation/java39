import java.util.*;
class App31 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);

		System.out.print("Lower limit:");
		var ll=sc.nextInt();

		System.out.print("Upper limit:");
		var ul=sc.nextInt();

		for(var i=ll;i<=ul;i++)
		{
			System.out.println(i);
		}

	}
}
